/*Declare three constants called rating1 , rating2 and rating3 of type double and assign each
a value. Calculate the average of the three and store the result in a constant named
averageRating */

void main() {
  const double rating1 = 8;
  const double rating2 = 4;
  const double rating3 = 7;
  const double average = (rating1 + rating2 + rating3) / 3;
  const double averageRating = average;
  print("The average ratind is $averageRating");
}
